/// 创建一个简单的 "Hello, World!" 程序的 X IR
pub fn create_hello_world() -> Program {
    let mut program = Program::new();

    // printf 声明
    program.add(Declaration::ExternFunction(ExternFunction {
        name: "printf".to_string(),
        return_type: Type::Int,
        parameters: vec![Type::Pointer(Box::new(Type::Char))],
    }));

    // main 函数
    let mut main = Function::new("main", Type::Int);

    // printf("Hello, World!\n")
    let printf_call = Expression::Call(
        Box::new(Expression::Variable("printf".to_string())),
        vec![Expression::Literal(Literal::String("Hello, World!\\n".to_string()))],
    );

    main.add_stmt(Statement::Expression(printf_call));
    main.add_stmt(Statement::Return(Some(Expression::Literal(Literal::Integer(0)))));

    program.add(Declaration::Function(main));

    program
}

/// 创建一个简单的 Fibonacci 程序的 X IR
pub fn create_fibonacci() -> Program {
    let mut program = Program::new();

    // printf 声明
    program.add(Declaration::ExternFunction(ExternFunction {
        name: "printf".to_string(),
        return_type: Type::Int,
        parameters: vec![Type::Pointer(Box::new(Type::Char))],
    }));

    // fib(n) = n <= 1 ? n : fib(n-1) + fib(n-2)
    let mut fib = Function::new("fib", Type::Int);
    fib = fib.param("n", Type::Int);

    // n <= 1
    let cond = Expression::Binary(
        BinaryOp::LessThanEqual,
        Box::new(Expression::Variable("n".to_string())),
        Box::new(Expression::Literal(Literal::Integer(1))),
    );

    // n - 1
    let n_minus_1 = Expression::Binary(
        BinaryOp::Subtract,
        Box::new(Expression::Variable("n".to_string())),
        Box::new(Expression::Literal(Literal::Integer(1))),
    );

    // n - 2
    let n_minus_2 = Expression::Binary(
        BinaryOp::Subtract,
        Box::new(Expression::Variable("n".to_string())),
        Box::new(Expression::Literal(Literal::Integer(2))),
    );

    // fib(n-1)
    let fib_n_minus_1 = Expression::Call(
        Box::new(Expression::Variable("fib".to_string())),
        vec![n_minus_1],
    );

    // fib(n-2)
    let fib_n_minus_2 = Expression::Call(
        Box::new(Expression::Variable("fib".to_string())),
        vec![n_minus_2],
    );

    // fib(n-1) + fib(n-2)
    let sum = Expression::Binary(
        BinaryOp::Add,
        Box::new(fib_n_minus_1),
        Box::new(fib_n_minus_2),
    );

    // if (n <= 1) { return n; } else { return fib(n-1) + fib(n-2); }
    let mut then_block = Block::new();
    then_block.add(Statement::Return(Some(Expression::Variable("n".to_string()))));

    let mut else_block = Block::new();
    else_block.add(Statement::Return(Some(sum)));

    let if_stmt = Statement::If(IfStatement {
        condition: cond,
        then_branch: Box::new(Statement::Compound(then_block)),
        else_branch: Some(Box::new(Statement::Compound(else_block))),
    });

    fib.add_stmt(if_stmt);
    program.add(Declaration::Function(fib));

    // main 函数
    let mut main = Function::new("main", Type::Int);

    // for (int i = 0; i < 10; i++) printf("fib(%d) = %d\n", i, fib(i));
    let mut for_body = Block::new();

    let printf_call = Expression::Call(
        Box::new(Expression::Variable("printf".to_string())),
        vec![
            Expression::Literal(Literal::String("fib(%d) = %d\\n".to_string())),
            Expression::Variable("i".to_string()),
            Expression::Call(
                Box::new(Expression::Variable("fib".to_string())),
                vec![Expression::Variable("i".to_string())],
            ),
        ],
    );

    for_body.add(Statement::Expression(printf_call));

    let for_stmt = Statement::For(ForStatement {
        initializer: Some(Box::new(Statement::Variable(
            Variable::new("i", Type::Int).init(Expression::Literal(Literal::Integer(0)))
        ))),
        condition: Some(Expression::Binary(
            BinaryOp::LessThan,
            Box::new(Expression::Variable("i".to_string())),
            Box::new(Expression::Literal(Literal::Integer(10))),
        )),
        increment: Some(Expression::Unary(
            UnaryOp::PostIncrement,
            Box::new(Expression::Variable("i".to_string())),
        )),
        body: Box::new(Statement::Compound(for_body)),
    });

    main.add_stmt(for_stmt);
    main.add_stmt(Statement::Return(Some(Expression::Literal(Literal::Integer(0)))));

    program.add(Declaration::Function(main));

    program
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_hello_world_generation() {
        let program = create_hello_world();
        let mut backend = CBackend::new(CBackendConfig::default());
        let c_code = backend.generate_from_xir(&program).unwrap();
        assert!(c_code.contains("Hello, World!"));
        assert!(c_code.contains("int main()"));
    }

    #[test]
    fn test_fibonacci_generation() {
        let program = create_fibonacci();
        let mut backend = CBackend::new(CBackendConfig::default());
        let c_code = backend.generate_from_xir(&program).unwrap();
        assert!(c_code.contains("int fib("));
        assert!(c_code.contains("printf("));
    }
}
