use serde::{Deserialize, Serialize};
use std::path::Path;

#[derive(Debug, Clone, Serialize, Deserialize, Default)]
pub struct Lockfile {
    pub version: u32,
    #[serde(default, rename = "package")]
    pub packages: Vec<LockedPackage>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LockedPackage {
    pub name: String,
    pub version: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub source: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub checksum: Option<String>,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub dependencies: Vec<String>,
}

impl Lockfile {
    pub fn new() -> Self {
        Lockfile {
            version: 1,
            packages: Vec::new(),
        }
    }

    pub fn from_path(path: &Path) -> Result<Self, String> {
        let content = std::fs::read_to_string(path)
            .map_err(|e| format!("无法读取 {}: {}", path.display(), e))?;
        toml::from_str(&content).map_err(|e| format!("解析 {} 失败: {}", path.display(), e))
    }

    pub fn save(&self, path: &Path) -> Result<(), String> {
        let header = "# This file is auto-generated by `x` — do not edit by hand.\n\n";
        let content = toml::to_string_pretty(self)
            .map_err(|e| format!("序列化锁文件失败: {}", e))?;
        std::fs::write(path, format!("{}{}", header, content))
            .map_err(|e| format!("无法写入 {}: {}", path.display(), e))
    }

    pub fn find_package(&self, name: &str) -> Option<&LockedPackage> {
        self.packages.iter().find(|p| p.name == name)
    }

    pub fn add_or_update(&mut self, pkg: LockedPackage) {
        if let Some(existing) = self.packages.iter_mut().find(|p| p.name == pkg.name) {
            *existing = pkg;
        } else {
            self.packages.push(pkg);
        }
        self.packages.sort_by(|a, b| a.name.cmp(&b.name));
    }

    pub fn remove(&mut self, name: &str) {
        self.packages.retain(|p| p.name != name);
    }
}
